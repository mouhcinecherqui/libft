#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		sign;

	sign = (n < 0 ? -1 : 1);
	if (n / 10 != 0)
	{
		ft_putnbr_fd(n / 10, fd);
	}
	if (sign == -1 && n / 10 == 0)
		ft_putchar_fd('-', fd);
	ft_putchar_fd((n % 10) * sign + '0', fd);
}
